import numpy as np

n1 = np.array([10,20,30,40,50])
n1 = n1*2	#for Multiplication

print('Multiplying array with 2 :')
print(n1)

n1 = n1+2	#for Addition

print('Adding array with 2 :')
print(n1)

n1 = n1/2	#for Division

print('Dividing array with 2 :')
print(n1)

n1 = n1 - 2	#for Subtraction
print('Subtracting array with 2 :')
print(n1)